#include <string.h>
/**
 * _strcmp - compares two strings
 *
 * @s1: pointer to main string
 * @s2: pointer to comparison string
 *
 * Description: compares s1 to s2; return 1 if more, 0 if equal, else -1
 *
 * Return: int
 */

int _strcmp(char *s1, char *s2)
{
	return (strcmp(s1, s2));
}
